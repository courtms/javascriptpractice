let day = 'Wednesday';

day = day.toLocaleLowerCase();
let daysToSat; 

switch (day) {
    case 'sunday':
        daysToSat = 6;
    case 'monday':
        daysToSat = 5;
    case 'tuesday':
        daysToSat = 4;
    case 'wednesday':
        daysToSat = 3;
    case 'thursday':
        daysToSat = 2;










}